var n = parseInt(prompt("Введите натуральное число n:"));

// Проверка, что n является натуральным числом больше 1
if (isNaN(n) || n <= 1) {
    console.log("Пожалуйста, введите натуральное число больше 1.");
} else {
    console.log("Простые числа в интервале от 2 до " + n + ":");
    
    // Перебираем числа от 2 до n
    for (var i = 2; i <= n; i++) {
        var isPrime = true;

        // Проверяем, является ли число i простым
        for (var j = 2; j < i; j++) {
            if (i % j === 0) {
                isPrime = false;
                break;
            }
        }

        // Если число простое, выводим его
        if (isPrime) {
            console.log(i);
        }
    }
}
